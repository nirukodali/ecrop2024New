package com.ecrops.entity;

public class LockDropdown {
	
	private int code;
	private String concat;
	private Character group_reason;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public String getConcat() {
		return concat;
	}
	public void setConcat(String concat) {
		this.concat = concat;
	}
	public Character getGroup_reason() {
		return group_reason;
	}
	public void setGroup_reason(Character group_reason) {
		this.group_reason = group_reason;
	}
	

}
